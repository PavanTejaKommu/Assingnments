package com.dataTypes;

public class CorrectDataType {

	public static void main(String[] args) {
		byte a =10;
		System.out.println(" For 10 byte because it is a small whole number and within byte range \n ");

		short b=200;
		System.out.println(" For 200 , short it is greater than byte range and less than short max value \n");

		int c = 50000;
		System.out.println(" For 50000 int  it is greater than short range and less than int max value \n");

		long d = 10000000000L;
		System.out.println(" For 10000000000 long  it is greater than int range and less than long max value \n");

		float f = 95.5F;
		System.out.println(" For 95.5 float because its decial number \n ");

		double dd = 3.14159265359;
		System.out.println(" For  3.14159265359 Double  because it can store long decimal values\n");

		char chr = 'A';
		System.out.println(" For A char because it stores a single character\n");
		
		boolean d3 = true;
		System.out.println("For true boolean because it stores only true or false values  ");



	}

}
