package com.dataTypes;

public class PrimitiveDataTypes {

	public static void main(String[] args) {

		byte age = 20;

		short marks = 500;

		int salary = 25000;

		long population = 1400000000L;

		float percentage = 85.5f;

		double pi = 3.14159265359;

		char grade = 'A';

		boolean isPassed = true;

		System.out.println("byte data type        "+age );

		System.out.println("short data type       "+marks);

		System.out.println(  "int data type         "+salary);

		System.out.println("long data type        "+population);

		System.out.println("float data type       "+percentage);

		System.out.println(  "double data type      "+pi);

		System.out.println( "char data type        "+grade);

		System.out.println( "boolean data type     "+isPassed);

	}

}
